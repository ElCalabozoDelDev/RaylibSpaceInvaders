Obstacle::Obstacle(Vector2 pos)
{
    this->position = pos;
    

    for(unsigned int i = 0; i < grid.size(); i++)
    {
        for(unsigned int j = 0; j < grid[i].size(); j++)
        {
            if(grid[i][j] == 1)
            {
                blocks.push_back(Block({position.x + j * 3, position.y + i * 3}));
            }
        }
    }
}

void Obstacle::Draw()
{
    for(auto& block : blocks)
    {
        block.Draw();
    }   
}